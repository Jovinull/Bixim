//     bit3 = column 1
//     bit2 = column 2
//     bit1 = column 3
//     bit0 = column 4 (rightmost pixel)
//
// Example — letter 'A':
//   Row 0: .###.  = 0b01110 = 0x0E
//   Row 1: #...#  = 0b10001 = 0x11
//   Row 2: #...#  = 0x11
//   Row 3: #####  = 0b11111 = 0x1F
//   Row 4: #...#  = 0x11
//   Row 5: #...#  = 0x11
//   Row 6: #...#  = 0x11
//
// Rendered character dimensions: 5 wide x 7 tall.
// Suggested advance (including spacing): 6 pixels (5px glyph + 1px gap).
// =============================================================================
